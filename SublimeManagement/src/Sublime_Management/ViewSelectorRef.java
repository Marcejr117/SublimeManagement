    private void btnSeleccinarRefActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccinarRefActionPerformed
        try {
            if (receptor == 1) {
                String ref = (String) tbSelectorRef.getModel().getValueAt(tbSelectorRef.getSelectedRow(), 1);
                aux.setRefCompra(ref);
                this.dispose();
            }
            if (receptor == 2) {
                String ref = (String) tbSelectorRef.getModel().getValueAt(tbSelectorRef.getSelectedRow(), 1);
                aux.setRefVenta(ref);
                
                String precioP = Productos.getProductoByReference2(ref).getPvp();
                aux.setPrecioVenta(precioP+" â‚¬");
                aux.setPrecioFinal();
                this.dispose();
            }

        } catch (java.lang.ArrayIndexOutOfBoundsException ex) {
        }
    }//GEN-LAST:event_btnSeleccinarRefActionPerformed

